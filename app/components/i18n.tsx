export type T = Record<string, string>;

const I18nContext = createContext<{ t: T; lang: Lang; setLang: (l: Lang) => void }>({
  t: translations.ru as T,
  lang: "ru",
  setLang: () => {},
});

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("ru");

  // Загружаем сохранённый язык (переживает переходы между страницами)
  useEffect(() => {
    const stored = localStorage.getItem(LANG_KEY) as Lang | null;
    if (stored === "ru" || stored === "kz" || stored === "en") setLangState(stored);
  }, []);

  const setLang = (l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(LANG_KEY, l); } catch { /* ignore */ }
  };

  return (
    <I18nContext.Provider value={{ t: translations[lang] as T, lang, setLang }}>
      {children}
    </I18nContext.Provider>
  );
}

export function useI18n() {
  return useContext(I18nContext);
}
